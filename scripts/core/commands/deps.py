# Mapping from tool.toml [presets] compiler names to Conan 2 settings.
_CONAN_COMPILER: dict[str, dict] = {
    "gcc":   {"compiler": "gcc",   "libcxx": "libstdc++11"},
    "clang": {"compiler": "clang", "libcxx": "libstdc++11"},
    "msvc":  {"compiler": "msvc",  "libcxx": None},
    "cuda":  {"compiler": "gcc",   "libcxx": "libstdc++11"},  # CUDA uses gcc by default
}

_CONAN_BUILD_TYPE: dict[str, str] = {
    "debug":          "Debug",
    "release":        "Release",
    "relwithdebinfo": "RelWithDebInfo",
    "minsizerel":     "MinSizeRel",
}

_CONAN_ARCH: dict[str, str] = {
    "x86_64":  "x86_64",
    "x86":     "x86",
    "aarch64": "armv8",
    "armv7":   "armv7hf",
}


def _detect_compiler_version(compiler: str) -> str:
    """Try to detect the installed compiler major version."""
    exe = "g++" if compiler == "gcc" else ("clang++" if compiler == "clang" else None)
    if not exe:
        return "0"
    try:
        r = subprocess.run([exe, "--version"], capture_output=True, text=True, timeout=5)
        for line in r.stdout.splitlines():
            import re
            m = re.search(r"(\d+)\.\d+\.\d+", line)
            if m:
                return m.group(1)
    except Exception:
        pass
    return "0"


def _cmd_conan_profile(args: argparse.Namespace) -> CLIResult:
    try:
        _impl_conan_profile_generate(args)
        return CLIResult(success=True, message="Conan profiles generated.")
    except SystemExit as e:
        return CLIResult(success=(e.code == 0), code=e.code or 1, message="Conan profile generation failed.")


def _impl_conan_profile_generate(args) -> None:
    """Generate Conan 2 profiles from tool.toml [presets] matrix.

    Each combination of (compiler, build_type, linkage, arch) that does not
    match a skip_combinations pattern produces one profile file named after
    the preset convention:  <compiler>-<build_type>-<linkage>-<arch>

    When Conan 2 is installed the profiles are written to ~/.conan2/profiles/.
    Otherwise the profile text is printed to stdout.
    """
    from core.utils.config_loader import load_tool_config
    import fnmatch
    import re as _re

    cfg = load_tool_config()
    presets_cfg: dict = cfg.get("presets", {})

    compilers   = presets_cfg.get("compilers",   ["gcc", "clang"])
    build_types = presets_cfg.get("build_types", ["debug", "release"])
    linkages    = presets_cfg.get("linkages",    ["static"])
    arches      = presets_cfg.get("arches",      ["x86_64"])
    skip_pats   = presets_cfg.get("skip_combinations", [])

    dry_run: bool = getattr(args, "dry_run", False)
    output_dir_arg: str | None = getattr(args, "output_dir", None)

    # Resolve output directory
    conan2_dir = Path.home() / ".conan2" / "profiles"
    if output_dir_arg:
        out_dir: Path | None = Path(output_dir_arg)
    elif shutil.which("conan"):
        out_dir = conan2_dir
    else:
        out_dir = None  # print to stdout

    generated: list[str] = []

    for compiler in compilers:
        for build_type in build_types:
            for linkage in linkages:
                for arch in arches:
                    name = f"{compiler}-{build_type}-{linkage}-{arch}"

                    # Apply skip_combinations patterns
                    skip = False
                    for pat in skip_pats:
                        if fnmatch.fnmatch(name, pat):
                            skip = True
                            break
                    if skip:
                        Logger.info(f"  [skip] {name} (matches skip_combinations)")
                        continue

                    comp_map = _CONAN_COMPILER.get(compiler, {"compiler": compiler, "libcxx": "libstdc++11"})
                    bt_conan = _CONAN_BUILD_TYPE.get(build_type, build_type.capitalize())
                    arch_conan = _CONAN_ARCH.get(arch, arch)
                    compiler_ver = _detect_compiler_version(compiler)
                    libcxx = comp_map.get("libcxx")
                    conan_compiler = comp_map["compiler"]

                    lines = [
                        f"# Conan 2 profile: {name}",
                        f"# Generated by `tool deps conan-profile generate` from tool.toml [presets]",
                        "",
                        "[settings]",
                        "os=Linux",
                        f"arch={arch_conan}",
                        "compiler=" + conan_compiler,
                        f"compiler.version={compiler_ver}",
                        "compiler.cppstd=17",
                    ]
                    if libcxx:
                        lines.append(f"compiler.libcxx={libcxx}")
                    lines.append(f"build_type={bt_conan}")
                    lines.append("")
                    lines.append("[conf]")
                    lines.append("tools.build:sharedlinkflags=[]")
                    if linkage == "static":
                        lines.append("tools.build:exelinkflags=[]")
                    lines.append("")

                    profile_text = "\n".join(lines)

                    if out_dir is None or dry_run:
                        # Print to stdout (no conan installed or dry-run)
                        separator = "─" * 60
                        print(f"\n{separator}")
                        print(f"Profile: {name}")
                        print(separator)
                        print(profile_text)
                    else:
                        out_dir.mkdir(parents=True, exist_ok=True)
                        profile_path = out_dir / name
                        profile_path.write_text(profile_text, encoding="utf-8")
                        Logger.success(f"  Written: {profile_path}")

                    generated.append(name)

    if not generated:
        Logger.warn("[Conan profile] No profiles generated (check tool.toml [presets] matrix and skip_combinations).")
        return

    Logger.info(f"[Conan profile] {len(generated)} profile(s) generated: {', '.join(generated)}")
    if out_dir is None:
        Logger.info("[Conan profile] Conan not in PATH — profiles printed above. Install conan2 to auto-write profiles.")
    elif dry_run:
        Logger.info(f"[Conan profile] DRY-RUN — would write to {conan2_dir}")


# ---------------------------------------------------------------------------
# Parser
# ---------------------------------------------------------------------------

def deps_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(prog="tool deps", description="Dependency lock file management")
    sub = parser.add_subparsers(dest="subcommand")

    # lock
    p = sub.add_parser("lock", help="Generate lock files for all dependency managers")
    p.add_argument("--dry-run", action="store_true", dest="dry_run",
                   help="Preview what would be written without creating files")
    p.add_argument("--managers", nargs="+", default=["vcpkg", "conan", "pip"],
                   choices=["vcpkg", "conan", "pip"],
                   help="Which managers to lock (default: all)")
    p.set_defaults(func=_cmd_lock)

    # verify
    p = sub.add_parser("verify", help="Verify lock files are present and consistent")
    p.set_defaults(func=_cmd_verify)

    # list
    p = sub.add_parser("list", help="List dependencies from all manifests")
    p.set_defaults(func=_cmd_list)

    # conan-profile
    p = sub.add_parser("conan-profile", help="Generate Conan 2 profiles from tool.toml [presets] matrix")
    sub_cp = p.add_subparsers(dest="conan_profile_action")
    p_gen = sub_cp.add_parser("generate", help="Generate and write/print Conan 2 profiles")
    p_gen.add_argument("--output-dir", default=None, metavar="DIR", dest="output_dir",
                       help="Write profiles to this directory (default: ~/.conan2/profiles or stdout if conan absent)")
    p_gen.add_argument("--dry-run", action="store_true", dest="dry_run",
                       help="Print profiles to stdout without writing files")
    p_gen.set_defaults(func=_cmd_conan_profile)
    # Support `tool deps conan-profile generate` but also bare `tool deps conan-profile`
    p.set_defaults(func=_cmd_conan_profile)

    return parser


def main(argv: list[str]) -> None:
    parser = deps_parser()
    args = parser.parse_args(argv if argv else [])
    if hasattr(args, "func"):
        args.func(args).exit()
    else:
        parser.print_help()
