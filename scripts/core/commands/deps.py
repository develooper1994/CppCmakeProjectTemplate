#!/usr/bin/env python3
"""
core/commands/deps.py — Dependency management: lock files for vcpkg, Conan, and pip.

Subcommands:
  lock   — generate lock files for all detected dependency managers
  verify — check that lock files are present and up-to-date
  list   — list dependencies from all manifests
"""
from __future__ import annotations

import argparse
import hashlib
import json
import shutil
import subprocess
from pathlib import Path

from core.utils.common import Logger, CLIResult, run_proc, PROJECT_ROOT

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

VCPKG_JSON = PROJECT_ROOT / "vcpkg.json"
VCPKG_LOCK = PROJECT_ROOT / "vcpkg.lock.json"
CONAN_FILE = PROJECT_ROOT / "conanfile.py"
CONAN_LOCK = PROJECT_ROOT / "conan.lock"
REQ_FILE = PROJECT_ROOT / "requirements-dev.txt"
PIP_LOCK = PROJECT_ROOT / "requirements-dev.lock.txt"


def _sha256_file(path: Path) -> str:
    h = hashlib.sha256()
    h.update(path.read_bytes())
    return h.hexdigest()


# ---------------------------------------------------------------------------
# vcpkg lock
# ---------------------------------------------------------------------------

def _lock_vcpkg(dry_run: bool) -> bool:
    """Generate vcpkg.lock.json from vcpkg.json manifest."""
    if not VCPKG_JSON.exists():
        Logger.info("  vcpkg.json not found — skipping")
        return True

    Logger.info(f"  vcpkg.json detected: {VCPKG_JSON.name}")
    manifest = json.loads(VCPKG_JSON.read_text(encoding="utf-8"))
    dependencies = manifest.get("dependencies", [])

    # Normalize dependencies to name+version-constraint records
    lock_entries: list[dict] = []
    for dep in dependencies:
        if isinstance(dep, str):
            lock_entries.append({"name": dep, "version-constraint": "*"})
        elif isinstance(dep, dict):
            lock_entries.append({
                "name": dep.get("name", "?"),
                "version-constraint": dep.get("version>=", dep.get("version", "*")),
                "features": dep.get("features", []),
                "platform": dep.get("platform", ""),
            })

    # Try to enrich with installed versions via `vcpkg list --x-json`
    vcpkg_bin = shutil.which("vcpkg")
    installed: dict[str, str] = {}
    if vcpkg_bin:
        try:
            result = subprocess.run(
                [vcpkg_bin, "list", "--x-json"],
                capture_output=True, text=True, timeout=30
            )
            if result.returncode == 0 and result.stdout.strip():
                vcpkg_list = json.loads(result.stdout)
                for pkg_id, info in vcpkg_list.items():
                    # pkg_id format: "zlib:x64-linux" or "zlib@1.2.11#0:x64-linux"
                    name = pkg_id.split(":")[0].split("@")[0]
                    version = info.get("version", "*")
                    installed[name] = version
        except (subprocess.TimeoutExpired, json.JSONDecodeError, OSError):
            pass

    for entry in lock_entries:
        if entry["name"] in installed:
            entry["resolved-version"] = installed[entry["name"]]

    lock = {
        "$comment": "Auto-generated by 'tool deps lock'. Do not edit manually.",
        "manifest-hash": _sha256_file(VCPKG_JSON),
        "dependencies": lock_entries,
    }

    if dry_run:
        Logger.info(f"  [dry-run] Would write {VCPKG_LOCK}")
        Logger.info(f"  {len(lock_entries)} dependency entries")
        return True

    VCPKG_LOCK.write_text(json.dumps(lock, indent=2, sort_keys=False), encoding="utf-8")
    Logger.info(f"  Written: {VCPKG_LOCK} ({len(lock_entries)} entries)")
    return True


# ---------------------------------------------------------------------------
# Conan lock
# ---------------------------------------------------------------------------

def _lock_conan(dry_run: bool) -> bool:
    """Generate conan.lock using 'conan lock create' or a static snapshot."""
    if not CONAN_FILE.exists():
        Logger.info("  conanfile.py not found — skipping")
        return True

    Logger.info(f"  conanfile.py detected: {CONAN_FILE.name}")
    conan_bin = shutil.which("conan")

    if conan_bin:
        Logger.info("  Running: conan lock create .")
        if dry_run:
            Logger.info(f"  [dry-run] Would run: conan lock create . --lockfile-out {CONAN_LOCK}")
            return True
        rc = run_proc(
            [conan_bin, "lock", "create", ".",
             "--lockfile-out", str(CONAN_LOCK)],
            cwd=str(PROJECT_ROOT), check=False
        )
        if rc == 0:
            Logger.info(f"  Written: {CONAN_LOCK}")
        else:
            Logger.warn("  'conan lock create' failed — writing manifest-hash snapshot")
            _write_conan_lock_snapshot(dry_run)
        return rc == 0
    else:
        Logger.warn("  conan not found — writing manifest-hash snapshot (install conan for full lock)")
        return _write_conan_lock_snapshot(dry_run)


def _write_conan_lock_snapshot(dry_run: bool) -> bool:
    lock = {
        "$comment": "Snapshot lock — install conan and run 'tool deps lock' for full lock.",
        "manifest-hash": _sha256_file(CONAN_FILE),
        "conan-version": "2.x",
    }
    if dry_run:
        Logger.info(f"  [dry-run] Would write {CONAN_LOCK}")
        return True
    CONAN_LOCK.write_text(json.dumps(lock, indent=2), encoding="utf-8")
    Logger.info(f"  Written: {CONAN_LOCK}")
    return True


# ---------------------------------------------------------------------------
# pip lock
# ---------------------------------------------------------------------------

def _lock_pip(dry_run: bool) -> bool:
    """Generate pinned requirements lock from requirements-dev.txt via pip-compile or pip freeze."""
    if not REQ_FILE.exists():
        Logger.info("  requirements-dev.txt not found — skipping")
        return True

    Logger.info(f"  requirements-dev.txt detected")

    pip_compile = shutil.which("pip-compile")
    if pip_compile:
        Logger.info("  Running: pip-compile requirements-dev.txt")
        if dry_run:
            Logger.info(f"  [dry-run] Would write {PIP_LOCK}")
            return True
        rc = run_proc(
            [pip_compile, str(REQ_FILE), "--output-file", str(PIP_LOCK),
             "--quiet", "--no-header"],
            cwd=str(PROJECT_ROOT), check=False
        )
        if rc == 0:
            Logger.info(f"  Written: {PIP_LOCK}")
            return True

    # Fallback: pip freeze (only current env packages)
    Logger.warn("  pip-compile not found (install pip-tools). Using 'pip freeze' fallback.")
    if dry_run:
        Logger.info(f"  [dry-run] Would write {PIP_LOCK}")
        return True
    result = subprocess.run(["pip", "freeze"], capture_output=True, text=True)
    if result.returncode == 0:
        header = "# Generated by 'tool deps lock' — pip freeze snapshot\n"
        PIP_LOCK.write_text(header + result.stdout, encoding="utf-8")
        Logger.info(f"  Written: {PIP_LOCK}")
        return True

    Logger.error("  pip freeze failed")
    return False


