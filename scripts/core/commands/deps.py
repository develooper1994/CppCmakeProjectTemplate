def _cmd_lock(args) -> CLIResult:
    """Generate lock files for all detected dependency managers."""
    dry_run: bool = getattr(args, "dry_run", False)
    managers: list[str] = getattr(args, "managers", ["vcpkg", "conan", "pip"])

    Logger.info("Generating dependency lock files...")
    if dry_run:
        Logger.info("  (dry-run mode — no files written)\n")

    results = {}

    if "vcpkg" in managers:
        Logger.info("\n[vcpkg]")
        results["vcpkg"] = _lock_vcpkg(dry_run)

    if "conan" in managers:
        Logger.info("\n[conan]")
        results["conan"] = _lock_conan(dry_run)

    if "pip" in managers:
        Logger.info("\n[pip]")
        results["pip"] = _lock_pip(dry_run)

    all_ok = all(results.values())
    msg = "Lock files generated" if all_ok else "Some lock files failed"
    Logger.info(f"\n{'✓' if all_ok else '✗'} {msg}: {results}")
    return CLIResult(success=all_ok, code=0 if all_ok else 1, message=msg, data=results)


def _cmd_verify(args) -> CLIResult:
    """Verify lock files are present and consistent with their manifests."""
    issues: list[str] = []

    # vcpkg
    if VCPKG_JSON.exists():
        if not VCPKG_LOCK.exists():
            issues.append("vcpkg.lock.json missing — run 'tool deps lock'")
        else:
            lock = json.loads(VCPKG_LOCK.read_text(encoding="utf-8"))
            if lock.get("manifest-hash") != _sha256_file(VCPKG_JSON):
                issues.append("vcpkg.lock.json is stale (vcpkg.json changed) — run 'tool deps lock'")
            else:
                Logger.info("  ✓ vcpkg.lock.json up-to-date")

    # conan
    if CONAN_FILE.exists():
        if not CONAN_LOCK.exists():
            issues.append("conan.lock missing — run 'tool deps lock'")
        else:
            lock = json.loads(CONAN_LOCK.read_text(encoding="utf-8"))
            current_hash = _sha256_file(CONAN_FILE)
            stored_hash = lock.get("manifest-hash", "")
            if stored_hash and stored_hash != current_hash:
                issues.append("conan.lock is stale (conanfile.py changed) — run 'tool deps lock'")
            else:
                Logger.info("  ✓ conan.lock up-to-date")

    # pip
    if REQ_FILE.exists():
        if not PIP_LOCK.exists():
            Logger.info("  ℹ requirements-dev.lock.txt absent — run 'tool deps lock --managers pip' with pip-tools installed")
        else:
            Logger.info("  ✓ requirements-dev.lock.txt present")

    if issues:
        for iss in issues:
            Logger.error(f"  ✗ {iss}")
        return CLIResult(success=False, code=1, message=f"{len(issues)} lock issue(s) found", data={"issues": issues})

    return CLIResult(success=True, message="All lock files present and up-to-date")


def _cmd_list(args) -> CLIResult:
    """List dependencies from all manifests."""
    all_deps: dict[str, list] = {}

    # vcpkg
    if VCPKG_JSON.exists():
        manifest = json.loads(VCPKG_JSON.read_text(encoding="utf-8"))
        deps = manifest.get("dependencies", [])
        all_deps["vcpkg"] = deps
        Logger.info("[vcpkg]")
        for d in deps:
            name = d if isinstance(d, str) else d.get("name", "?")
            Logger.info(f"  {name}")

    # conan — parse require() calls from conanfile.py
    if CONAN_FILE.exists():
        Logger.info("\n[conan]")
        conan_deps = []
        for line in CONAN_FILE.read_text(encoding="utf-8").splitlines():
            stripped = line.strip()
            if stripped.startswith("self.requires("):
                dep = stripped.removeprefix("self.requires(").strip().strip(')"').strip("'")
                conan_deps.append(dep)
                Logger.info(f"  {dep}")
        all_deps["conan"] = conan_deps

    # pip
    if REQ_FILE.exists():
        Logger.info("\n[pip]")
        pip_deps = []
        for line in REQ_FILE.read_text(encoding="utf-8").splitlines():
            stripped = line.strip()
            if stripped and not stripped.startswith("#"):
                pip_deps.append(stripped)
                Logger.info(f"  {stripped}")
        all_deps["pip"] = pip_deps

    if not all_deps:
        return CLIResult(success=False, code=1, message="No dependency manifests found")

    total = sum(len(v) for v in all_deps.values())
    return CLIResult(success=True, message=f"{total} dependencies found", data=all_deps)


